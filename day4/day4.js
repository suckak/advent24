const matrix = input.split(/\r?\n|\r|\n/g);

const searchTerm = 'XMAS';

const checkPosition = (x, y, matrix) => {
  //tiene que llegar un char X

  const lx = matrix[0].length;
  const ly = matrix.length;

  let matches = 0;

  // derecha - 3
  if (lx - (y + 3) >= 0) {
    const word = matrix[x].substring(y, y + 4);
    if (word === searchTerm) {
      matches++;
    }

    // SE - 1
    if (x + 4 <= ly) {
      const word =
        matrix[x][y] +
        matrix[x + 1][y + 1] +
        matrix[x + 2][y + 2] +
        matrix[x + 3][y + 3];
      if (word === searchTerm) {
        matches++;
      }
    }

    // NE - 4
    if (x > 2) {
      const word =
        matrix[x][y] +
        matrix[x - 1][y + 1] +
        matrix[x - 2][y + 2] +
        matrix[x - 3][y + 3];
      if (word === searchTerm) {
        matches++;
      }
    }
  }

  // izquierda - 2
  if (y >= 3) {
    const word = matrix[x].substring(y - 3, y + 1);
    if (word.split('').reverse().join('') === searchTerm) {
      matches++;
    }

    // NW -
    if (x > 2) {
      const word =
        matrix[x][y] +
        matrix[x - 1][y - 1] +
        matrix[x - 2][y - 2] +
        matrix[x - 3][y - 3];
      if (word === searchTerm) {
        matches++;
      }
    }

    // SW -
    if (x + 4 <= ly) {
      const word =
        matrix[x][y] +
        matrix[x + 1][y - 1] +
        matrix[x + 2][y - 2] +
        matrix[x + 3][y - 3];
      if (word === searchTerm) {
        matches++;
      }
    }
  }

  // abajo - 1
  if (x + 4 <= ly) {
    const word =
      matrix[x][y] + matrix[x + 1][y] + matrix[x + 2][y] + matrix[x + 3][y];
    if (word === searchTerm) {
      matches++;
    }
  }

  // arriba - 2
  if (x > 2) {
    const word =
      matrix[x][y] + matrix[x - 1][y] + matrix[x - 2][y] + matrix[x - 3][y];
    if (word === searchTerm) {
      matches++;
    }
  }

  return matches;
};

let result = 0;

for (let i = 0; i < matrix.length; i++) {
  for (let j = 0; j < matrix.length; j++) {
    const char = matrix[i][j];

    if (char === 'X') {
      result += checkPosition(i, j, matrix);
    }
  }
}

console.log('total: ', result);
